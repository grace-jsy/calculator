package calculator;

public class Calculator {

    int add(int a, int b) {
        int result = a + b;
        return result;
    }

    int subtract(int a, int b) {
        int result = a - b;
        return result;
    }

    int multiply(int a, int b) {
        int result = a * b;
        return result;
    }

    int divide(int a, int b) {
        int result = a / b;
        return result;
    }






}

